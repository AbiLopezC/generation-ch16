public class OperadoresIncrementales {
    public static void main(String[] args) {
        //preincremento
        int i=1;
        int j= --i;
        System.out.println("i = " + i);
        System.out.println("j = " + j);
        //pos incremento

        i=2;
        j=i--;
        System.out.println("i = " + i);
        System.out.println("j = " + j);
    }
}
